#Variables in Python

# Declaracion e inicializacion de variables
age = 28
high = 1.75
country = 'Mexico'

# Acceso a las variables
print("Valores inicialeles")
print("Age:", age)
print("High:", high)
print("Country:", country)

# Modificar el valor de una variable
age = 30
high = 1.80

print("Valores modificados")
print("Age:", age)
print("High:", high)
print("Country:", country)

# En Python el tipo es dinamico
age = 'Treinta'
print("age:", age)

# Si queremos acceder a una variable que no ha sido declarada manda error
phone = 123456789
print("Phone:", phone)